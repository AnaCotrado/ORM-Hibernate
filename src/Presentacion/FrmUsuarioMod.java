    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        
        if(txtDni.getText().equals("")){
             JOptionPane.showMessageDialog(this, "Ingrese Dni","Mensaje",JOptionPane.WARNING_MESSAGE);
            txtDni.requestFocus();
            
        }else if(txtNombre.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese Nombre","Mensaje",JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocus();
        }
        else if(txtApellido.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese Apellidos","Mensaje",JOptionPane.WARNING_MESSAGE);
            txtApellido.requestFocus();
        }
        else if(txtLogin.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese Usuario","Mensaje",JOptionPane.WARNING_MESSAGE);
            txtLogin.requestFocus();
        }
        else if(txtPassword.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese Contrase√±a","Mensaje",JOptionPane.WARNING_MESSAGE);
            txtPassword.requestFocus();
        }
        else if(txtNivel.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese Nivel","Mensaje",JOptionPane.WARNING_MESSAGE);
            txtNivel.requestFocus();
        }else{
            N_Usuario func = new N_Usuario();
            
            func.modificarUsuario(Integer.parseInt(txtIdUsuario.getText()),txtDni.getText(),txtNombre.getText(),txtApellido.getText(),txtLogin.getText(),txtPassword.getText(),Short.parseShort(txtNivel.getText()));
            JOptionPane.showMessageDialog(this, "Editado correctamente");
            
            FrmUsuario ventana = new FrmUsuario();
            FrmUsuario.usuario.setIdUsuario(0);
            ventana.setVisible(true);
            this.dispose();          
        } 
    }//GEN-LAST:event_btnAceptarActionPerformed

