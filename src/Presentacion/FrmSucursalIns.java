    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if(cboAdministrador.getSelectedItem()=="Seleccionar"){
            JOptionPane.showMessageDialog(this, "Seleecione Id Usuario","Mensaje",JOptionPane.WARNING_MESSAGE);
            
        }else if(txtNombre.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese Nombre","Mensaje",JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocus();
            
        }else if(txtDireccion.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese Direccion","Mensaje",JOptionPane.WARNING_MESSAGE);
            txtDireccion.requestFocus();
            
        }else{
            func.agregarSucursal(txtNombre.getText(), txtDireccion.getText(), Integer.parseInt(cboAdministrador.getSelectedItem().toString()));
            
            JOptionPane.showMessageDialog(this, "Registrado correctamente");
        }
        
        new FrmSucursal().setVisible(true);
        FrmSucursal.sucursal.setIdSucursal(0);
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

